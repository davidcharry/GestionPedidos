    private void ComprarButtonPanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComprarButtonPanActionPerformed
        
        int fila = TablePan.getSelectedRow();
        
        DefaultTableModel mode1 = (DefaultTableModel) TablePan.getModel();
        DefaultTableModel mode2 = (DefaultTableModel) InfoTable.getModel();

        String Tipo = "Pantalon";
        String nombre = mode1.getValueAt(fila, 0).toString();
        double precio = Double.valueOf((String) mode1.getValueAt(fila, 1));
        int cantidad = 1;

        mode2.addRow(new Object[]{Tipo, nombre, precio, cantidad});
        
        TablePan.clearSelection();

    }//GEN-LAST:event_ComprarButtonPanActionPerformed

